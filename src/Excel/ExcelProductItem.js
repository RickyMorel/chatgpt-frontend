class ExcelProductItem {
    constructor(name, code, description, price, tags, amount, imageLink, catalogueLink) {
      this.name = name;
      this.code = code;
      this.description = description;
      this.price = price;
      this.tags = tags;
      this.amount = amount;
      this.imageLink = imageLink;
      this.catalogueLink = catalogueLink;
    }
}

export default ExcelProductItem